  -- Auto-formatting with conform.nvim
  {
    "stevearc/conform.nvim",
    opts = {
      formatters_by_ft = {
        lua = { "stylua" },
        python = { "isort", "black" },
        javascript = { "prettier" },
        typescript = { "prettier" },
        javascriptreact = { "prettier" },
        typescriptreact = { "prettier" },
        vue = { "prettier" },
        svelte = { "prettier" },
        css = { "prettier" },
        scss = { "prettier" },
        less = { "prettier" },
        html = { "prettier" },
        json = { "prettier" },
        jsonc = { "prettier" },
        yaml = { "prettier" },
        markdown = { "prettier" },
        graphql = { "prettier" },
      },
      format_on_save = function(bufnr)
        -- Disable formatting for certain file types that commonly fail
        local disable_filetypes = { "sql", "java" }
        local filetype = vim.bo[bufnr].filetype
        if vim.tbl_contains(disable_filetypes, filetype) then
          return
        end
        
        -- Check if prettier is available for this file
        local conform = require("conform")
        local formatters = conform.list_formatters(bufnr)
        if #formatters == 0 then
          return
        end
        
        return {
          timeout_ms = 3000,
          lsp_fallback = true,
        }
      end,
    },
  },

